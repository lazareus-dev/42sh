#include "libft.h"

char	*ft_strstr(const char *haystack, const char *needle)
{
	int		i;
	int		j;
	char	*p;

	p = (char *)haystack;
	if (!*needle)
		return (p);
	i = 0;
	while (haystack[i])
	{
		j = 0;
		while (haystack[i + j] == needle[j])
		{
			if (needle[j + 1] == '\0')
			{
				return (p + i);
			}
			j++;
		}
		i++;
	}
	return (NULL);
}
