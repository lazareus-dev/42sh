#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	a;
	char			*p;

	p = (char *)b;
	a = (unsigned char)c;
	i = 0;
	while (i < len)
		p[i++] = a;
	return (p);
}
